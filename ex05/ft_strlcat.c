int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return (i);
}

unsigned int	ft_strlcat(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	des;
	unsigned int	sr;

	i = ft_strlen(dest);
	des = ft_strlen(dest);
	sr = ft_strlen(src);
	j = 0;
	if (size < 1)
		return (sr + size);
	while (src[j] && i < size - 1)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	if (size < des)
		return (sr + size);
	else
		return (des + sr);
}
